function colorchange(id) {

    var background = document.getElementById(id).style.backgroundColor;
    if (background == "rgb(0, 201, 255)") {
        document.getElementById(id).style.background = "rgb(162, 238, 154)";
    } else {
        document.getElementById(id).style.background = "rgb(0, 201, 255)";
    }

}

